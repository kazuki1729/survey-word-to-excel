"""
デモ用のサンプル調査票（Word .docx）を生成するスクリプト。
※ 実在の調査とは無関係の、ポートフォリオ用ダミーデータです。

    python make_sample.py
"""

from pathlib import Path
from docx import Document

TITLE = "働き方と健康に関する意識調査（サンプル）"
INTRO = "本調査はポートフォリオ用に作成した架空のアンケートです。以下の設問にお答えください。"

# (設問文, [選択肢...]) ／ 選択肢が空のものは自由記述
QUESTIONS = [
    ("あなたの年齢を教えてください。", ["20歳未満", "20〜29歳", "30〜39歳", "40〜49歳", "50歳以上"]),
    ("あなたの性別を教えてください。", ["男性", "女性", "回答しない"]),
    ("現在の就業形態を教えてください。", ["正社員", "契約・派遣社員", "パート・アルバイト", "自営業・フリーランス", "無職・学生"]),
    ("1週間あたりの平均的な労働時間はどのくらいですか。", ["20時間未満", "20〜39時間", "40〜49時間", "50時間以上"]),
    ("テレワーク（在宅勤務）の頻度を教えてください。", ["ほぼ毎日", "週に数回", "月に数回", "ほとんどない"]),
    ("現在の仕事に満足していますか。", ["とても満足", "やや満足", "どちらともいえない", "やや不満", "とても不満"]),
    ("仕事上のストレスを感じることはありますか。", ["よくある", "ときどきある", "あまりない", "まったくない"]),
    ("睡眠時間は1日平均どのくらいですか。", ["5時間未満", "5〜6時間", "7〜8時間", "8時間以上"]),
    ("運動する習慣はありますか。", ["週3回以上", "週1〜2回", "月に数回", "ほとんどしない"]),
    ("健康のために気をつけていることがあれば教えてください。（複数選択可）",
     ["食事のバランス", "適度な運動", "十分な睡眠", "ストレス管理", "特になし"]),
    ("勤務先に健康支援の制度（健診・相談窓口等）はありますか。", ["ある", "ない", "わからない"]),
    ("今後、働き方について改善したい点はありますか。", []),
]


def main():
    doc = Document()
    doc.add_heading(TITLE, level=1)
    doc.add_paragraph(INTRO)
    doc.add_paragraph("")

    for i, (text, options) in enumerate(QUESTIONS, start=1):
        doc.add_paragraph(f"問{i}. {text}")
        for j, opt in enumerate(options, start=1):
            doc.add_paragraph(f"{j}. {opt}")
        doc.add_paragraph("")

    out = Path("sample/sample_survey.docx")
    out.parent.mkdir(exist_ok=True)
    doc.save(out)
    print(f"サンプル調査票を作成しました: {out}（{len(QUESTIONS)}問）")


if __name__ == "__main__":
    main()
